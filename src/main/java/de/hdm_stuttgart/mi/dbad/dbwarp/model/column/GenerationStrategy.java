public enum GenerationStrategy {
  /**
   * The column is not generated
   */
  NONE,
  /**
   * The column is automatically incremented by a sequence. If rows are deleted, the values are not
   * guaranteed to be unique (i.e. deleted values may be reused)
   */
  SERIAL,
  /**
   * The column is generated by a sequence, and it is guaranteed that the values are unique even if
   * rows are deleted
   */
  IDENTITY
}
